# ============================================================
# Helper: convert kΩ values to uint32_t ×100
# ============================================================
def to_x100(row):
    return [int(round(v * 100)) for v in row]

# ============================================================
# 1. Generate raw binary file for W25Q80 (littlefs)
#    Format: uint32_t LE, row-major, (rh_idx * 11 + temp_idx) * 4
#    Size: 76 × 11 × 4 = 3,344 bytes
# ============================================================
