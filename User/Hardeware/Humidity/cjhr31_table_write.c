/**
 * @brief 将 CJHR-31 查表数据写入 littlefs
 *
 * 写入 /sys/rh_table.bin, 二进制 uint32_t LE, row-major.
 * 共 76 × 11 × 4 = 3,344 字节.
 *
 * @return 0 成功, -1 失败
 */
int cjhr31_table_write_to_lfs(void)
{
    lfs_file_t file;
    int err;

    /* 确保 /sys/ 目录存在 */
    err = lfs_mkdir(&lfs, "/sys");
    if (err && err != LFS_ERR_EXIST) {
        return -1;
    }

    /* 打开文件 (创建/覆写) */
    err = lfs_file_open(&lfs, &file, CJHR31_TABLE_PATH,
                        LFS_O_WRONLY | LFS_O_CREAT | LFS_O_TRUNC);
    if (err) {
        return -1;
    }

    /* 逐行写入全部数据 */
    for (int row = 0; row < CJHR31_RH_ROWS; row++) {
        for (int col = 0; col < CJHR31_TEMP_COLS; col++) {
            uint32_t val = cjhr31_table_data[row][col];
            /* little-endian: 先写低字节 */
            uint8_t buf[4];
            buf[0] = (uint8_t)(val & 0xFF);
            buf[1] = (uint8_t)((val >> 8) & 0xFF);
            buf[2] = (uint8_t)((val >> 16) & 0xFF);
            buf[3] = (uint8_t)((val >> 24) & 0xFF);

            lfs_size_t n = lfs_file_write(&lfs, &file, buf, 4);
            if (n != 4) {
                lfs_file_close(&lfs, &file);
                return -1;
            }
        }
    }

    lfs_file_sync(&lfs, &file);
    lfs_file_close(&lfs, &file);

    return 0;   /* 写入成功 */
}

